package 서병범.test;

public class Math_test {

    public static void main(String[] args) {
        int a = -10;
        int absValue = Math.abs(a); // absValue = 10
        /*
        Math.abs() :
        주어진 숫자의 절댓값을 반환합니다.
        Math.abs()는 다양한 데이터 타입을 지원합니다: int, long, float, double.
         */


        System.out.println(absValue);
    }
}
